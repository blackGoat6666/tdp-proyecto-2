package ParOrdenado;

public class ParOrdenado {
	//atributos 
	private int x, y;
	
	//constructor
	public ParOrdenado (int x1, int y1) {
		x = x1;
		y = y1;
	}
	
	//metodos
	public void setX (int x1) {
		x = x1;
	}

	public void setY (int y1) {
		y = y1;
	}
	
	//consultas
	
	public int getX () {
		return x;
	}
	
	public int getY () {
		return y;
	}
}
